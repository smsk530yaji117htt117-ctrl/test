#!/usr/bin/env python3
"""Meeting Result Processor MVP.

Reads an AI Consensus Log meeting result (Markdown), classifies it, and
when the classification is dev_task, emits a Handoff v2 Markdown that
can be fed into relay/create_handoff_page.py.
"""
from __future__ import annotations

import argparse
import datetime as _dt
import os
import sys
from pathlib import Path

HUMAN_REVIEW_KEYWORDS = [
    "承認", "支払い", "APIキー", "API キー", "削除", "本番",
    "Billing", ".env", "Secrets", "環境変数", "カード", "課金",
]
DEV_TASK_KEYWORDS = [
    "実装", "修正", "コード", "PR", "スクリプト", "ファイル",
    "関数", "テスト", "バグ",
]
RESEARCH_TASK_KEYWORDS = [
    "調査", "比較", "確認", "リサーチ", "情報収集", "検証",
]

CATEGORIES = ("dev_task", "research_task", "human_review", "no_action")


def classify(text: str) -> str:
    """Rule-based classifier. human_review wins over dev_task."""
    hit_human = any(k in text for k in HUMAN_REVIEW_KEYWORDS)
    hit_dev = any(k in text for k in DEV_TASK_KEYWORDS)
    hit_research = any(k in text for k in RESEARCH_TASK_KEYWORDS)

    if hit_human:
        return "human_review"
    if hit_dev:
        return "dev_task"
    if hit_research:
        return "research_task"
    return "no_action"


HANDOFF_TEMPLATE = """# Handoff v2: {task}

- Task: {task}
- Status: Draft
- From AI: Meeting Result Processor
- To AI: not determined
- Repository: not determined
- Working Directory: not determined
- Target Files: not determined
- Execution Environment: not determined
- Git Managed: not determined
- Goal: {goal}
- Current State: Generated from AI Consensus Log meeting result on {ts}.
- Next Action: Review meeting body and execute the described change.
- Do Not Touch: .env / APIキー / Billing / 本番データ
- Acceptance Criteria: Human review required
- Escalation Rule: Escalate to Human if Do Not Touch items appear.
- Touched Files: not determined
- Risks: Human review required
- Commands Run: not determined
- Notes: Auto-generated by meeting_result_processor.py

## Source Meeting Result

{body}
"""


def build_handoff_markdown(body: str) -> str:
    first_line = next(
        (ln.strip("# ").strip() for ln in body.splitlines() if ln.strip()),
        "Untitled meeting task",
    )
    ts = _dt.datetime.now().strftime("%Y-%m-%d %H:%M:%S")
    return HANDOFF_TEMPLATE.format(
        task=first_line[:120],
        goal=first_line[:200],
        ts=ts,
        body=body.strip(),
    )


def write_handoff(body: str, out_dir: Path) -> Path:
    out_dir.mkdir(parents=True, exist_ok=True)
    stamp = _dt.datetime.now().strftime("%Y%m%d_%H%M%S")
    path = out_dir / f"handoff_from_meeting_{stamp}.md"
    path.write_text(build_handoff_markdown(body), encoding="utf-8")
    return path


def process(text: str, out_dir: Path) -> dict:
    category = classify(text)
    result = {"category": category, "handoff_path": None}
    if category == "dev_task":
        result["handoff_path"] = str(write_handoff(text, out_dir))
    return result


def _read_file(path: str) -> str:
    return Path(path).read_text(encoding="utf-8")


def _read_page(page_id: str) -> str:
    raise NotImplementedError(
        "--page-id is not implemented in this MVP. Use --from-file."
    )


def main(argv: list[str] | None = None) -> int:
    p = argparse.ArgumentParser(
        description="Classify an AI Consensus Log meeting result and "
                    "optionally emit a Handoff v2 Markdown."
    )
    src = p.add_mutually_exclusive_group(required=True)
    src.add_argument("--from-file", help="Path to a meeting result Markdown.")
    src.add_argument("--page-id", help="(Not implemented) Notion page id.")
    p.add_argument(
        "--out-dir",
        default="generated_handoffs",
        help="Where dev_task Handoff v2 Markdown is written.",
    )
    args = p.parse_args(argv)

    if args.page_id:
        print("ERROR: --page-id is not implemented in this MVP.",
              file=sys.stderr)
        return 2

    text = _read_file(args.from_file)
    result = process(text, Path(args.out_dir))

    print(f"category: {result['category']}")
    if result["handoff_path"]:
        print(f"handoff:  {result['handoff_path']}")
    else:
        print("handoff:  (none generated)")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
